export function getServiceDisplayName(locale: Locale, serviceName: string, fallback?: string) {
  const key = serviceName.toLowerCase() as keyof typeof serviceNameMap;
  return serviceNameMap[key]?.[locale] ?? fallback ?? serviceName;
}
